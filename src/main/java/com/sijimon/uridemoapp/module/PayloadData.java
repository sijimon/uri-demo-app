package com.sijimon.uridemoapp.module;



public class PayloadData {

	private String baseurl;
	private String encryptpasscode;
	private String payload;
	
	
	
	public PayloadData() {
	}
	
	public String getBaseurl() {
		return baseurl;
	}
	public void setBaseurl(String baseurl) {
		this.baseurl = baseurl;
	}
	public String getEncryptpasscode() {
		return encryptpasscode;
	}
	public void setEncryptpasscode(String encryptpasscode) {
		this.encryptpasscode = encryptpasscode;
	}
	public String getPayload() {
		return payload;
	}
	public void setPayload(String payload) {
		this.payload = payload;
	}
	
	

}
